// mathematical operations between numeric operands.

//(=) Assinment Operator.
//Assigns right operand value to left operand:
var a = 10;

//(+) Additional Operator
//Adds two numeric operands:
var a = 10;
var b = 20;
var c =a+b;
console.log(c); //output: 30
//sort

var a = "hello";
var b = "world";
var c = a+b;
console.log(c); //output: helloworld

//(-) Subtract Operator
//Subtract right operand from left operand:
var a = 20;
var b = 10;
var c = a-b;
console.log(c); //output: 10

//(*) Multiply Operator
//Multiply two numeric operands:
var a = 10;
var b = 10;
var c = a*b;
console.log(c); //output: 100

//(/) Divide Operator
//Divide left operand by right operand.:
var a = 10;
var b = 10;
var c = a/b;
console.log(c); //output: 1

//(%) Modulus Operator
//Modulus operator. Returns remainder of two operands:
var a = 10;
var b = 10;
var c = a%b;
console.log(c); //output: 0

